				<h2 id="Colectas">Scientific collection in Argentina</h2>
					In Argentina it is utterly forbidden the collection of any component of the fauna and flora without the authorization of the competent authorities. As a country of provincial federal organization, each province has its specific legislation on this subject, and in each case it is necessary to consult local limitations for the collection and transportation of any specimen. Each person must first have the provincial authorization in order to be able to apply for the national exportation permit. In general, collections are authorized only if they are justified by research projects supported by scientific institutions within the Argentine system.
